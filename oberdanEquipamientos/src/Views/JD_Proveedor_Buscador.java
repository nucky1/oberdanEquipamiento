    public void actualizarTabla(ArrayList<Proveedor> list) {
        DefaultTableModel modelo = (DefaultTableModel) tabla_proveedores_busqueda.getModel();
        listBusquedaProveedores = list;
        modelo.setNumRows(0);
        Object[] obj= new Object[3];
        for(int i  = 0; i < listBusquedaProveedores.size(); i++){
            obj[0] = listBusquedaProveedores.get(i).getId();
            obj[1] = listBusquedaProveedores.get(i).getNombre();
            obj[2] = listBusquedaProveedores.get(i).getContacto();
            modelo.addRow(obj);
        } 
        
    }
    
    
    public abstract void getIDProveedor(int id, String atributo);
    
     public void cambioBusqueda(String txt){
            if(txt.isEmpty()){
                actualizarTabla(controler_buscador.buscarProveedorReducido(atributo,""));
            }else if(rbutton_buscar_codigo.isSelected()){
                try{
                    int cod = Integer.parseInt(txt);
                    controler_buscador.buscarProveedor(cod);
                }catch(Exception ex){
                    JOptionPane.showMessageDialog(null,"Debe ingresar un código", 
                        "Error",  JOptionPane.ERROR_MESSAGE);
                    input_buscar_proveedor.setText("");
                }
            }else if(rbutton_buscar_nombre.isSelected()){
                actualizarTabla(controler_buscador.buscarProveedorReducido(atributo, txt.toLowerCase()));
            }else{
                System.out.println("Error no selecciono tipo de busqueda");
            }
        }

    private void seleccionar() {
        if(tabla_proveedores_busqueda.getSelectedRow()!= -1){
            int pos = tabla_proveedores_busqueda.getSelectedRow();
            getIDProveedor(listBusquedaProveedores.get(pos).getId(), 
                    listBusquedaProveedores.get(pos).getNombre());
            dispose();
        }
        else
            JOptionPane.showMessageDialog(null,"Debe seleccionar un proveedor", "Error",  JOptionPane.ERROR_MESSAGE);        
                    
    }
